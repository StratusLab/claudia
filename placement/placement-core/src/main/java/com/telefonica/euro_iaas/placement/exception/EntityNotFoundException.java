package com.telefonica.euro_iaas.placement.exception;

@SuppressWarnings("serial")
public class EntityNotFoundException extends Exception {
	/**
     * Constructor of the class.
     *
     */
	public EntityNotFoundException() {
		super();
	}

	/**
	 * Constructor of the class.
	 * 
	 * @param arg0 the message
	 * @param arg1 the parent exception
	 */
	public EntityNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	/**
	 * Constructor of the class.
	 * 
	 * @param arg0 the message
	 */
	public EntityNotFoundException(String arg0) {
		super(arg0);
	}

	/**
	 * Constructor of the class.
	 * 
	 * @param arg0 the parent exception
	 */
	public EntityNotFoundException(Throwable arg0) {
		super(arg0);
	}
}
